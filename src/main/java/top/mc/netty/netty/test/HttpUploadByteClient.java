package top.mc.netty.netty.test;

import io.netty.bootstrap.Bootstrap;
import io.netty.channel.ChannelFuture;
import io.netty.channel.ChannelInitializer;
import io.netty.channel.ChannelOption;
import io.netty.channel.EventLoopGroup;
import io.netty.channel.nio.NioEventLoopGroup;
import io.netty.channel.socket.SocketChannel;
import io.netty.channel.socket.nio.NioSocketChannel;
import top.mc.netty.util.PropUtil;

/**
 * This class is meant to be run against {@link HttpUploadByteClient}.
 */
public final class HttpUploadByteClient {
    /**
     * 请求服务器地址
     */
    private final String BASE_URL = PropUtil.getV("baseUrlServer");
    public HttpUploadByteClient(){

    }
    public  static void main(String[] args) throws InterruptedException {
        String host ="127.0.0.1";
        int port = 7002;
        EventLoopGroup workerGroup = new NioEventLoopGroup();

        try {
            Bootstrap b = new Bootstrap(); // (1)
            b.group(workerGroup); // (2)
            b.channel(NioSocketChannel.class); // (3)
            b.option(ChannelOption.SO_KEEPALIVE, true); // (4)
            b.handler(new ChannelInitializer<SocketChannel>() {
                @Override
                public void initChannel(SocketChannel ch) throws Exception {
                    ch.pipeline().addLast("fileByteClient",new FileByteClient()).addLast("fileClientHandler",new FileClientHandler());
                }
            });

            // Start the client.
            ChannelFuture f = b.connect(host, port).sync(); // (5)
            // Wait until the connection is closed.
            f.channel().closeFuture().sync();
        } finally {
            workerGroup.shutdownGracefully();
        }
    }

}
